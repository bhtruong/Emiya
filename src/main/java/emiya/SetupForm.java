package emiya;

public class SetupForm {
    private String Legendary;
    private String DarkCity;
    private String FantasticFour;


    public String getLegendary() {
        return Legendary;
    }

    public void setLegendary(String legendary) {
        Legendary = legendary;
    }

    public String getDarkCity() {
        return DarkCity;
    }

    public void setDarkCity(String darkCity) {
        DarkCity = darkCity;
    }

    public String getFantasticFour() {
        return FantasticFour;
    }

    public void setFantasticFour(String fantasticFour) {
        FantasticFour = fantasticFour;
    }
}
